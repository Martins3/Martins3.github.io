def cpuflags(guest, host):
    guest_set=set(guest.split())
    host_set=set(host.split())

    guest_only = guest_set - host_set
    host_only = host_set - guest_set
    print("Guest独有的特征:")
    print(sorted(guest_only))

    print("\nHost独有的特征:")
    print(sorted(host_only))

